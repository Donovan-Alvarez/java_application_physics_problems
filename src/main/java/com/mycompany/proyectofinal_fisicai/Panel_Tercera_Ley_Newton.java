    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String seleccion = jComboBox1.getSelectedItem().toString();
            if (seleccion.equals("Fuerza de reacción")) {

                String fuerzaAccion = jTextField1.getText();
                var resultado = calcularFuerzaReaccion(Double.parseDouble(fuerzaAccion));
                jTextField5.setText(String.valueOf(resultado));
                jTextField4.setText(String.valueOf(resultado));
                jTextField6.setText("-fuerzaDeAcción");

            } else if (seleccion.equals("Fuerza de acción")) {

                String fuerzaReaccion = jTextField1.getText();
                var resultado = calcularFuerzaAccion(Double.parseDouble(fuerzaReaccion));
                jTextField5.setText(String.valueOf(resultado));
                jTextField4.setText(String.valueOf(resultado));
                jTextField6.setText("-fuerzaDeReacción");
                
            } else if (seleccion.equals("Masa")) {

                String fuerza = jTextField1.getText();
                String aceleracion = jTextField4.getText();
                jTextField4.isEnabled();
                var resultado = calcularMasa(Double.parseDouble(fuerza), Double.parseDouble(aceleracion));
                jTextField5.setText(String.valueOf(resultado));
                jTextField6.setText("fuerza / aceleración");

            } else if (seleccion.equals("Aceleración")) {

                String fuerza = jTextField1.getText();
                String masa = jTextField4.getText();
                var resultado = calcularAceleracion(Double.parseDouble(fuerza), Double.parseDouble(masa));
                jTextField5.setText(String.valueOf(resultado));
                jTextField6.setText("fuerza / masa");

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error, revise los datos ingresados", "", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

