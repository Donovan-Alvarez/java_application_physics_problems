    public static double calcularFuerzaReaccion(double fuerzaAccion) {
        return -fuerzaAccion;
    }

    public static double calcularFuerzaAccion(double fuerzaReaccion) {
        return -fuerzaReaccion;
    }

    public static double calcularMasa(double fuerza, double aceleracion) {
        if (aceleracion == 0) {
            throw new IllegalArgumentException("La aceleración no puede ser cero.");
        }
        return fuerza / aceleracion;
    }

    public static double calcularAceleracion(double fuerza, double masa) {
        if (masa == 0) {
            throw new IllegalArgumentException("La masa no puede ser cero.");
        }
        return fuerza / masa;
    }

